package product;

public class TenderDTO {
	private Integer tender_num;
	private String tender_id;
	private Integer tender_price;
	public Integer getTender_num() {
		return tender_num;
	}
	public void setTender_num(Integer tender_num) {
		this.tender_num = tender_num;
	}
	public String getTender_id() {
		return tender_id;
	}
	public void setTender_id(String tender_id) {
		this.tender_id = tender_id;
	}
	public Integer getTender_price() {
		return tender_price;
	}
	public void setTender_price(Integer tender_price) {
		this.tender_price = tender_price;
	}
	
}
